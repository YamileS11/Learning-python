#Tipos de datos en Python
#Python es un lenguaje dinamicamente tipado

#1. Númericos
##Enteros Z --> int
num1 = 42
print( "Num1: ", type (num1))
##Flotantes o decimales (Reales) => Float
num2 = 50.5
print ("Num2: ", type(num2))
##Complejos => Complex
num3 = 2j
print ("Num3: ", type(num3))

#2. Cadena
my_name = "Yamile"
print ("my_name", type (my_name))
my_lastname = "Chamorro"
print ("my_lastname", type (my_lastname))
profile = "Yamile es una persona disiplinada"
print ("profile", type (profile))
address =  'Carrera 8 # 13-42'
print ("address", type (address))

a = 1
b = 1
suma1 = a + b #Suma Aritmetica

c = '1'
d = '1'
suma2 = c + d #Suma de cadenas

print ("suma1:", suma1)
